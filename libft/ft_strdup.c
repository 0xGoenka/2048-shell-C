#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char		*str;
	int			i;

	i = 0;
	if ((str = malloc(sizeof(char) * ft_strlen(s1) + 1)))
	{
		while (s1[i])
		{
			str[i] = s1[i];
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	else
		return (0);
}
