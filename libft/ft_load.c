#include "libft.h"

void	ft_load(int i)
{
	int tmp;

	tmp = i;
	while (i * 80 / 100 > 0)
	{
		ft_putchar('#');
		i--;
	}
	ft_putchar('>');
	while ((80 - tmp * 80 / 100) > i)
	{
		ft_putchar('.');
		i++;
	}
	ft_putstr("\n                                         ~");
	ft_putnbr(tmp);
	ft_putstr("%~                                          ");
}
