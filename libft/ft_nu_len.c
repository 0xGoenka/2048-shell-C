#include "libft.h"

int	nu_len(int n, int base)
{
	int i;

	i = 0;
	if (base == 10)
	{
		if (n == 0)
			return (1);
		if (n < 0)
			n = -n;
		while (n > 0)
		{
			n /= 10;
			i++;
		}
	}
	if (base == 2)
	{
		while (n >= ft_power(2, i))
			i++;
	}
	return (i);
}
